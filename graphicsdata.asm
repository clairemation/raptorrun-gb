; load the graphics data from ROM to VRAM
macro LoadSpriteDataIntoVRAM
    LoadSeriesOfBytes SpritesData, SPRITES_ROM_END, _VRAM8000
endm

macro LoadBGDataIntoVRAM
    LoadSeriesOfBytes BGTilesData, BG_TILES_ROM_END, $9000
endm

macro LoadBGTileMapIntoVRAM
    LoadSeriesOfBytes BGTileMap, ROM_END, $9800
endm

; clear the OAM
macro InitOAM
    ld c, OAM_COUNT
    ld hl, _OAMRAM
    ld de, sizeof_OAM_ATTRS
    .init_oam\@
        ld [hl], 0
        add hl, de
        dec c
        jr nz, .init_oam\@
endm

macro InitPallettes
    ; init the palettes
    ld a, %11100100
    ld [rBGP], a
    ld [rOBP0], a
    ld a, %00011011
    ld [rOBP1], a
endm

macro SetGraphicsParameters
    ld a, LCDCF_ON | LCDCF_WIN9C00 | LCDCF_WINOFF | LCDCF_BG8800 | LCDCF_BG9800 | LCDCF_OBJ16 | LCDCF_OBJON | LCDCF_BGON
endm

macro EnableLCD
    ld [rLCDC], a
endm

section "level-graphics", romx, bank[1]
    InitGraphicsData:
        DisableLCD

        LoadSpriteDataIntoVRAM
        LoadBGDataIntoVRAM
        LoadBGTileMapIntoVRAM

        InitOAM


        ; init sprite 0
        copy [SPRITE_0_ADDRESS + OAMA_TILEID], 0
        copy [SPRITE_0_ADDRESS + OAMA_FLAGS], OAMF_PAL0
        copy [SPRITE_0_ADDRESS + OAMA_X], 40
        
        copy [SPRITE_0_RIGHTSIDE + OAMA_TILEID], 2
        copy [SPRITE_0_RIGHTSIDE + OAMA_FLAGS], OAMF_PAL0
        copy [SPRITE_0_RIGHTSIDE + OAMA_X], 48


        ;init scroll
        xor a
        ld [rSCX], a
        ld [rSCY], a

        InitPallettes
        SetGraphicsParameters

        ret


